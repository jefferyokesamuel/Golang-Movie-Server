	movies = append(movies, Movie{ID: "1", Isbn: "3452", Title: "The Legend", Director: &Director{Firstname: "John", Lastname: "Newton"}})
	movies = append(movies, Movie{ID: "2", Isbn: "2453", Title: "All The Boys", Director: &Director{Firstname: "Jeff", Lastname: "Green"}})

	r := mux.NewRouter()
	r.HandleFunc("/movies", getMovies).Methods("GET")
	r.HandleFunc("/movies/{id}", getMovie).Methods("GET")
